// ── Context & Hook ────────────────────────────────────────────
export const LangContext = createContext("fr");

export function useT() {
  const lang = useContext(LangContext);
  return (key, params) => {
    let s = T[lang]?.[key] || T.fr[key] || key;
    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        s = s.replaceAll(`{${k}}`, String(v));
      });
    }
    return s;
  };
}

// Plural helper: tp("notes.remarks", count) → "remarque" or "remarques"
export function useTP() {
  const lang = useContext(LangContext);
  return (key, count) => {
    const base = T[lang]?.[key] || T.fr[key] || key;
    const plural = T[lang]?.[key + "_plural"] || T.fr[key + "_plural"];
    return count !== 1 && plural ? plural : base;
  };
}
